export default function PixelIcon({ type, size = 16, color, className = '' }: PixelIconProps) {
  const grid = ICON_MAP[type]
  if (!grid) return null

  const pixelSize = size / 8
  const fill = color || 'currentColor'

  const rects: JSX.Element[] = []
  for (let y = 0; y < 8; y++) {
    for (let x = 0; x < 8; x++) {
      if (grid[y][x]) {
        rects.push(
          <rect
            key={`${x}-${y}`}
            x={x * pixelSize}
            y={y * pixelSize}
            width={pixelSize}
            height={pixelSize}
            fill={fill}
          />
        )
      }
    }
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${size} ${size}`}
      fill="none"
      className={className}
      style={{ imageRendering: 'pixelated' }}
    >
      {rects}
    </svg>
  )
}

// 导出图标类型供其他组件使用
export type PixelIconType = keyof typeof ICON_MAP
